package math;
import java.util.*;

public class Sqrt {

	public static void main(String[] args) {
		Scanner sc = new Scanner (System.in);
		
		System.out.println("Enter the value of a = ");
		int a = sc.nextInt();
		System.out.println("Square root of the " +a+ " is " +Math.sqrt(a));
		
	}
	
}
