package math;

public class RandomNum {

	public static void main(String[] args) {
		System.out.println(Math.random());//In this, value will in between 0 to 1 only
		System.out.println(Math.random() *101);//In this, value will in between 1 to 100 only
		System.out.println(Math.random() *201); //and so on 
	}

}
